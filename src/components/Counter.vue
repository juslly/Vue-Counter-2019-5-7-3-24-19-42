<template>
<div class="hello">
<h1>{{ msg }}</h1>
<button type="button" v-on:click="_add">+</button>
<button type="button" v-on:click="_minus">-</button>
<p>{{number}}</p >
</div>
</template>

<script>
export default {
name: 'Counter',
props: {
msg: String
},

data(){
return {
number: 0
}
},
methods:{
_add: function (){
this.number ++;
},

_minus: function (){
this.number --;
}
}
}
</script>

